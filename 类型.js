// 在 JavaScript 中，数据类型大致可以分为两类：原始数据类型和引用类型。

// 原始数据类型（Primitive Types）：

// 原始数据类型包括：

// Number：任何数值，包括整数和浮点数。
// String：一系列字符的集合，用于表示文本。
// Boolean：逻辑实体，可以是 true 或 false。
// Null：表示缺乏值或没有值。
// Undefined：在声明后未被初始化的变量的类型是 Undefined。
// BigInt：可以表示任意大的整数。
// Symbol：是一个唯一且不可变的数据类型，经常被用作对象的键值。
// 原始数据类型在赋值时会被复制，而不是被引用。这意味着如果你改变一个原始数据类型的值，其他的变量并不会随之改变。
// JavaScript中的引用类型包括：

// Object：Object类型是JavaScript中的基本对象类型，它是一种引用类型。
// Function：每个函数都是Function类型的实例，而且都与其他引用类型一样具有属性和方法。
// Array：Array类型是JavaScript中的数组对象，它是一种引用类型。
// RegExp：RegExp类型是JavaScript中用于表示正则表达式的对象，它是一种引用类型。
// Date：Date类型是JavaScript中的日期对象，它是一种引用类型。
// 除了上述引用类型，JavaScript中还有其他一些引用类型，例如基本包装类型（Boolean、Number和String），这些类型为了便于操作基本类型值。另外，单体内置对象（如Global和Math）也是引用类型。

